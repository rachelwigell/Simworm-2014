/**
 * enum to describe what compartment a cell is in on each axis
 * choices for x are anterior, xcenter, posterior
 * choices for y are dorsal, ycenter, and ventral
 * choices for z are left, zcenter, right
 */

package dataStructures;

public enum Compartment {
	ANTERIOR,
	XCENTER,
	POSTERIOR,
	DORSAL,
	YCENTER,
	VENTRAL,
	LEFT,
	ZCENTER,
	RIGHT
}